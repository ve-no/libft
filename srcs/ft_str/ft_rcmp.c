#include "libft.h"

int	ft_rcmp(char *s1, char *s2)
{
	int	len;

	len = (int)ft_strlen(s2);
	while (*s1)
		s1++;
	while (*s2)
		s2++;
	while (*s1 == *s2 && len)
	{
		s1--;
		s2--;
		len--;
	}
	return (*s1 - *s2);
}
